add_filter('gettext',              'yombal_translate', 20, 3);
add_filter('gettext_with_context', 'yombal_translate_ctx', 20, 4);
add_filter('ngettext',             'yombal_ntranslate', 20, 5);

function yombal_translate($t, $orig, $domain) {
    static $map = null;
    if ($map === null) $map = yombal_get_translation_map();
    if (isset($map[$orig])) return $map[$orig];
    return $t;
}
function yombal_translate_ctx($t, $orig, $ctx, $domain) {
    return yombal_translate($t, $orig, $domain);
}
function yombal_ntranslate($t, $s, $p, $n, $domain) {
    return yombal_translate($t, ($n==1?$s:$p), $domain);
}

